while 1==1:
      print ("Calc.py")
      onec=int(input("Enter onenumber  "))
      twoc=int(input("Enter twonumber  "))
      deistivie=input("Enter sign ")
      if  deistivie=="+":
            res1=onec+twoc
            print(res1)
      elif deistivie=="-":
            res2=onec-twoc
            print (res2)
      elif deistivie=="*":
            res3=onec*twoc
            print (res3)
      elif deistivie=="/":
            res3=onec/twoc
            print (res3)
      else:
            print ("ERROR!!")
